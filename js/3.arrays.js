//array 
let arr1 = [1,"hello",2,"bye"];
console.log(arr1);

//array operations 
//pop removes element from last 
arr1.pop();
console.log(arr1);

//push adds element in last
arr1.push("addedlast");
console.log(arr1);

//shift removes element from first 
arr1.shift();
console.log(arr1);

//unshift adds new element from first 
arr1.unshift("fromfirst");
console.log(arr1);
